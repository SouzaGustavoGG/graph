
package graph;

public class Coordinate {
    
    protected int posX;
    protected int posY;
    
    public Coordinate(int x, int y) {
        this.posX = x;
        this.posY = y;
    }

    public int getPosX() {
        return posX;
    }

    public int getPosY() {
        return posY;
    }
    
}
